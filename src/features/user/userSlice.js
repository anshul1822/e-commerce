import { createAsyncThunk, createSlice } from '@reduxjs/toolkit';
import { fetchLoggedInUserData, fetchLoggedInUserOrders, updateUser } from './userAPI';

const initialState = {
  userData : null,
  userOrders : null,
  status: 'idle',
  
};

